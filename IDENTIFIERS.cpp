// ---------------------------
// Identifiers in C++
// ---------------------------

// Identifiers are the names used to identify variables, functions, classes, objects, arrays, etc.

// Rules for writing identifiers:

// 1. Identifiers can include letters (A-Z, a-z), digits (0-9), and underscores (_).
//    Example: myVariable, _tempValue, number1

// 2. Identifiers must begin with a **letter** or an **underscore**.
//    Cannot start with a digit.
//    Invalid: 1name, 9total

// 3. Identifiers are **case-sensitive**.
//    Example: total, Total, and TOTAL are all different identifiers.

// 4. Identifiers must not be the same as **C++ reserved keywords**.
//    Example: int, class, return, new, public, etc. are all reserved and cannot be used as names.

