#include <iostream>
using namespace std;

int main() {
    int a=3;
    double b=34.90;
    char c='b';
    float d=90.87;
    bool e=true;
    bool f=false;

cout<<"Variable "<< a <<" is a type of INTEGER datatype"<<endl<<endl;
cout<<"Variable "<< b <<" is a type of DOUBLE datatype"<<endl<<endl;
cout<<"Variable "<< c <<" is a type of CHARACTER datatype"<<endl<<endl;
cout<<"Variable "<< d <<" is a type of FLOAT datatype"<<endl<<endl;
cout<<"Variable "<< e <<" is a type of BOOLEAN datatype"<<endl<<endl;
cout<<"Variable "<< f <<" is a type of BOOLEAN datatype"<<endl<<endl;

    return 0;
}

